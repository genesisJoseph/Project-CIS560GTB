DROP TABLE IF EXISTS EggSales;    -- References BatchEgg and Retail
DROP TABLE IF EXISTS BatchEgg;    -- References BirdGroup
DROP TABLE IF EXISTS BirdGroup;-- References Farm and BirdBreed
DROP TABLE IF EXISTS Retail;      -- Parent table
DROP TABLE IF EXISTS Farm;        -- Parent table
DROP TABLE IF EXISTS BirdBreed;-- Parent table

CREATE TABLE Farm (
    FarmID INT IDENTITY(1,1) PRIMARY KEY,
    Location NVARCHAR(100) NOT NULL,
    Name NVARCHAR(100) NOT NULL UNIQUE,
    PhoneNumber NVARCHAR(20) UNIQUE,
    Owner NVARCHAR(100)
);

CREATE TABLE BirdBreed (
    BirdBreedID INT IDENTITY(1,1) PRIMARY KEY,
    BreedName NVARCHAR(100) NOT NULL UNIQUE,
    Species NVARCHAR(100) NOT NULL
);

CREATE TABLE BirdGroup (
    BirdGroupID INT IDENTITY(1,1) PRIMARY KEY,
    Confinement NVARCHAR(100) NOT NULL,
    FarmID INT NOT NULL,
    BirdBreedID INT NOT NULL,

    CONSTRAINT FK_BirdGroup_Farm
        FOREIGN KEY (FarmID) REFERENCES Farm(FarmID),

    CONSTRAINT FK_BirdGroup_BirdBreed
        FOREIGN KEY (BirdBreedID) REFERENCES BirdBreed(BirdBreedID)
);
CREATE TABLE Retail (
    RetailID INT IDENTITY(1,1) PRIMARY KEY,
    PhoneNumber NVARCHAR(20) UNIQUE,
    Name NVARCHAR(100) NOT NULL UNIQUE,
    RetailFee DECIMAL(10, 2) NOT NULL
);
Create Table BatchEgg (
    BatchEggID INT IDENTITY(1,1) PRIMARY KEY,
    BirdGroupID INT NOT NULL,
    Quantity INT NOT NULL,
    Size NVARCHAR(50) NOT NULL,
    DateProduced DATE NOT NULL,
    BestByDate DATE NOT NULL,
    Grade NVARCHAR(10) NOT NULL,
    EggColor NVARCHAR(50) NOT NULL,
    CONSTRAINT FK_BatchEgg_BirdGroup
        FOREIGN KEY (BirdGroupID) REFERENCES BirdGroup(BirdGroupID)
);
Create Table EggSales (
    EggSalesID INT PRIMARY KEY IDENTITY(1,1),
    UnitPrice DECIMAL(10, 2) NOT NULL,
    AvgUnit INT,
    BatchEggID INT NOT NULL, -- Matched to BatchEgg table
    RetailID INT NOT NULL,
    CanceledOn DATETIME NULL,
    CONSTRAINT FK_EggSale_BatchEgg
        FOREIGN KEY (BatchEggID) REFERENCES BatchEgg(BatchEggID),
    CONSTRAINT FK_EggSale_Retail
        FOREIGN KEY (RetailID) REFERENCES Retail(RetailID)
);
